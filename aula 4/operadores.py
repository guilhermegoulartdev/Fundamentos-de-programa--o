# 1. Operadores Matemáticos
numero_um = 20
numero_dois = 15
ano_atual = 2026
ano_nasc = 2000

idade = ano_atual - ano_nasc
print(idade)

soma = numero_um + numero_dois
print(soma)

subtracao = numero_um - numero_dois
print(subtracao)

multiplicacao = numero_um * numero_dois
print(multiplicacao)

divisao = numero_um / numero_dois 
print(divisao)

print(f"A soma entre {numero_um} + {numero_dois} e {soma}")

print(f"A subtracao entre {numero_um} - {numero_dois} e {subtracao}")

print(f"A divisao entre {numero_um} / {numero_dois} e {divisao}")

print(f"A multiplicacao entre {numero_um} * {numero_dois} e {multiplicacao}")

print(f"A minha idade e de {idade} anos")